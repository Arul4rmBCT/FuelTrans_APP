package com.bct.HOS.BOS.BO;

public class TankChartConfig {

	private String Tank_No;

	private String Tank_Volume;

	private String Tank_Height;

	public String getTank_No() {
		return Tank_No;
	}

	public void setTank_No(String Tank_No) {
		this.Tank_No = Tank_No;
	}

	public String getTank_Volume() {
		return Tank_Volume;
	}

	public void setTank_Volume(String Tank_Volume) {
		this.Tank_Volume = Tank_Volume;
	}

	public String getTank_Height() {
		return Tank_Height;
	}

	public void setTank_Height(String Tank_Height) {
		this.Tank_Height = Tank_Height;
	}

}
