package com.bct.HOS.BOS.BO;

public class VersionMaster {

	private String Soft_Type = null;
	private String Old_Version = null;
	private String Cur_Version = null;

	public String getSoft_Type() {
		return Soft_Type;
	}

	public void setSoft_Type(String soft_Type) {
		Soft_Type = soft_Type;
	}

	public String getOld_Version() {
		return Old_Version;
	}

	public void setOld_Version(String old_Version) {
		Old_Version = old_Version;
	}

	public String getCur_Version() {
		return Cur_Version;
	}

	public void setCur_Version(String cur_Version) {
		Cur_Version = cur_Version;
	}

}
