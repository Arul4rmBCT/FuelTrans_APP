package com.bct.HOS.Fleet;

public class CardDetai {

	private String identificationStatus;
	private String ruleId;
	private String identificationNo;
	private String identificationType;
	private String mappedDriverCode;
	private String driverStatus;
	private String allowedPrimaryFuel;
	private String validFromDate;
	private String validToDate;

	public String getIdentificationStatus() {
		return identificationStatus;
	}

	public void setIdentificationStatus(String identificationStatus) {
		this.identificationStatus = identificationStatus;
	}

	public String getRuleId() {
		return ruleId;
	}

	public void setRuleId(String ruleId) {
		this.ruleId = ruleId;
	}

	public String getIdentificationNo() {
		return identificationNo;
	}

	public void setIdentificationNo(String identificationNo) {
		this.identificationNo = identificationNo;
	}

	public String getIdentificationType() {
		return identificationType;
	}

	public void setIdentificationType(String identificationType) {
		this.identificationType = identificationType;
	}

	public String getMappedDriverCode() {
		return mappedDriverCode;
	}

	public void setMappedDriverCode(String mappedDriverCode) {
		this.mappedDriverCode = mappedDriverCode;
	}

	public String getDriverStatus() {
		return driverStatus;
	}

	public void setDriverStatus(String driverStatus) {
		this.driverStatus = driverStatus;
	}

	public String getAllowedPrimaryFuel() {
		return allowedPrimaryFuel;
	}

	public void setAllowedPrimaryFuel(String allowedPrimaryFuel) {
		this.allowedPrimaryFuel = allowedPrimaryFuel;
	}

	public String getValidFromDate() {
		return validFromDate;
	}

	public void setValidFromDate(String validFromDate) {
		this.validFromDate = validFromDate;
	}

	public String getValidToDate() {
		return validToDate;
	}

	public void setValidToDate(String validToDate) {
		this.validToDate = validToDate;
	}

}
