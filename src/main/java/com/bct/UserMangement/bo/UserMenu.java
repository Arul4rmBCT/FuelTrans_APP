package com.bct.UserMangement.bo;

public class UserMenu {

	private String SEQ_NO;
	private String SLNO;
	private String entityType;
	private String menuIcon;
	private String menuText;
	private String screenModule;
	private String screenSubModule;
	private String searchJS;
	public String getSEQ_NO() {
		return SEQ_NO;
	}
	public void setSEQ_NO(String sEQ_NO) {
		SEQ_NO = sEQ_NO;
	}
	public String getSLNO() {
		return SLNO;
	}
	public void setSLNO(String sLNO) {
		SLNO = sLNO;
	}
	public String getEntityType() {
		return entityType;
	}
	public void setEntityType(String entityType) {
		this.entityType = entityType;
	}
	public String getMenuIcon() {
		return menuIcon;
	}
	public void setMenuIcon(String menuIcon) {
		this.menuIcon = menuIcon;
	}
	public String getMenuText() {
		return menuText;
	}
	public void setMenuText(String menuText) {
		this.menuText = menuText;
	}
	public String getScreenModule() {
		return screenModule;
	}
	public void setScreenModule(String screenModule) {
		this.screenModule = screenModule;
	}
	public String getScreenSubModule() {
		return screenSubModule;
	}
	public void setScreenSubModule(String screenSubModule) {
		this.screenSubModule = screenSubModule;
	}
	public String getSearchJS() {
		return searchJS;
	}
	public void setSearchJS(String searchJS) {
		this.searchJS = searchJS;
	}
	
	
	
}
